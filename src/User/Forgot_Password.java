    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (isEmptyPass()) {
            String UserName = jTextField1.getText();
            String Email = jTextField2.getText();
            String newPassword = String.valueOf(jPasswordField1.getPassword());
            String repeatPassword = String.valueOf(jPasswordField2.getPassword());
            if (newPassword.equals(repeatPassword)) {
                user.updatePassword(UserName, Email, newPassword);
                new Login().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Password doesn't match", "Warning", 2);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

