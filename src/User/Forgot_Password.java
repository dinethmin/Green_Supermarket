    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (isEmpty()) {
            String UserName = jTextField1.getText();
            String Email = jTextField2.getText();
            String UserEmail = user.getEmail(UserName);
            if (Email.equals(UserEmail)) {
                JOptionPane.showMessageDialog(this, "Please enter your new password", "Welcome " + UserName + " ", 2);
                passwordPanel.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Invalid Email address for " + UserName + "", "Wrong Email", 2);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        char input = evt.getKeyChar();
        if (!(input < '0' || input > '9') && input != '\b') {
            evt.consume();
            JOptionPane.showMessageDialog(this, "UserName doesn't contain any numbers!", "Warning", 2);

        }
    }//GEN-LAST:event_jTextField1KeyTyped

